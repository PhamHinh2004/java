package find_In_String;

public class main {
	public static void main(String[] args) {
		String s1 = "Hinh";
		String s2 = "h";
		// indexOf(__) funtion
		System.out.println(s1.indexOf(s2));
		System.out.println( s1.indexOf("H", 0));
		// lastIndexOf(__) funtion
		System.out.println(s1.lastIndexOf(s2));
		
	}
}
