
public class HangPhim {
	private String Name;
	private String Nation;
	public HangPhim(String name, String nation) {
		super();
		Name = name;
		Nation = nation;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getNation() {
		return Nation;
	}
	public void setNation(String nation) {
		Nation = nation;
	}
	
}
