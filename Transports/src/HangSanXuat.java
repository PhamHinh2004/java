
public class HangSanXuat {
	protected String ten_hang_san_xuat;
	protected String nation;
	
	public HangSanXuat(String ten_hang_san_xuat, String nation) {
		this.ten_hang_san_xuat = ten_hang_san_xuat;
		this.nation = nation;
	}
	public String getTen_hang_san_xuat() {
		return ten_hang_san_xuat;
	}
	public void setTen_hang_san_xuat(String ten_hang_san_xuat) {
		this.ten_hang_san_xuat = ten_hang_san_xuat;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	
}
